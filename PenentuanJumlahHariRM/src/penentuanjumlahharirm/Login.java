    private void jButtonLoginMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonLoginMouseReleased
        if((jTextFieldUser.getText().equals("admin")) && 
                (String.valueOf(jPasswordField1.getPassword()).equals("admin"))){
            new PenentuanJumlahHari().setVisible(true);
            dispose();
        }else{
            JOptionPane.showMessageDialog(
                    null,
                    jTextFieldUser.getText() + 
                    ", Password anda salah ",
                    "Pesan Kesalahan",
                    JOptionPane.ERROR_MESSAGE);
                jTextFieldUser.setText("");
                jPasswordField1.setText("");
                jTextFieldUser.requestFocus();
        }
    }//GEN-LAST:event_jButtonLoginMouseReleased

